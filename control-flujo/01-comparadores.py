# COMPARADORES LOGICOS

print(1 > 2)  # 1 es mayor que 2? F
print(1 < 2)  # T
print(1 >= 2)  # F
print(1 <= 2)  # T
print(2 >= 2)  # 2 menor o igual a 2? True
print(2 <= 2)  # T
print(2 == 2)  # 2 es igual a 2? True
print(2 == 3)  # 2 es igual a 3? False
print(2 == "2")  # False
print(2 != "2")  # 2 es desigual "2"? True
print(2 != 2)  # 2 es desigual 2? False
